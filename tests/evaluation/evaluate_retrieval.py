"""
Retrieval-only evaluation: MRR@K, Recall@K, Hit Rate@K.
No LLM required — runs in ~30 seconds.

Usage:
    python tests/evaluation/evaluate_retrieval.py \
        --testset tests/evaluation/fixtures/testset.json \
        --k 5

The script compares retrieved chunks against ground-truth contexts
using text similarity (substring match + normalized overlap).
Run this whenever k, threshold, chunk_size, or chunk_overlap change.
"""
import argparse
import json
import os
import sys
from datetime import datetime

sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", ".."))


def _text_overlap(a: str, b: str) -> float:
    """Jaccard-like character overlap between two strings (case-insensitive)."""
    a_chars = set(a.lower())
    b_chars = set(b.lower())
    if not a_chars or not b_chars:
        return 0.0
    intersection = len(a_chars & b_chars)
    union = len(a_chars | b_chars)
    return intersection / union


def _is_match(retrieved_text: str, ground_truth_contexts: list[str], threshold: float = 0.5) -> bool:
    """True if any ground-truth context has >= threshold overlap with retrieved_text."""
    return any(_text_overlap(retrieved_text, gt) >= threshold for gt in ground_truth_contexts)


def evaluate_retrieval(testset_path: str, k: int, chroma_dir: str, embed_model: str):
    from src.embedding import Embedding
    from src.vector_store import ChromaStore

    with open(testset_path, encoding="utf-8") as f:
        testset = json.load(f)

    embedding_model = Embedding(model_name=embed_model)
    vector_store = ChromaStore(persist_directory=chroma_dir)

    k_values = [1, 3, 5, 10]
    hit_rates = {k_: [] for k_ in k_values}
    mrr_scores = {k_: [] for k_ in k_values}
    recall_scores = {k_: [] for k_ in k_values}

    print(f"Evaluating {len(testset)} questions...")

    for item in testset:
        question = item["question"]
        ground_truth_contexts = item.get("contexts", [])
        if not ground_truth_contexts:
            continue

        query_embedding = embedding_model.embed_query(question)
        retrieved_docs = vector_store.search(query_embedding, k=max(k_values))

        for k_ in k_values:
            top_k_docs = retrieved_docs[:k_]
            matches = [_is_match(doc.page_content, ground_truth_contexts) for doc in top_k_docs]

            # Hit Rate@K
            hit_rates[k_].append(1.0 if any(matches) else 0.0)

            # MRR@K
            rr = 0.0
            for rank, is_match in enumerate(matches, start=1):
                if is_match:
                    rr = 1.0 / rank
                    break
            mrr_scores[k_].append(rr)

            # Recall@K: fraction of ground-truth contexts covered
            covered = sum(
                1 for gt in ground_truth_contexts
                if any(_is_match(doc.page_content, [gt]) for doc in top_k_docs)
            )
            recall_scores[k_].append(covered / len(ground_truth_contexts))

    results = {}
    print("\n--- Retrieval Metrics ---")
    for k_ in k_values:
        n = len(hit_rates[k_])
        if n == 0:
            continue
        hr = sum(hit_rates[k_]) / n
        mrr = sum(mrr_scores[k_]) / n
        rec = sum(recall_scores[k_]) / n
        results[f"k={k_}"] = {"hit_rate": round(hr, 4), "mrr": round(mrr, 4), "recall": round(rec, 4)}
        print(f"  @{k_:2d}  Hit Rate: {hr:.4f}  MRR: {mrr:.4f}  Recall: {rec:.4f}")

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    output_dir = os.path.join(os.path.dirname(__file__), "results")
    os.makedirs(output_dir, exist_ok=True)
    output_path = os.path.join(output_dir, f"retrieval_{timestamp}.json")
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump({"timestamp": timestamp, "k_eval": k, "metrics": results}, f, ensure_ascii=False, indent=2)
    print(f"\nSaved → {output_path}")
    return results


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Retrieval-only RAG evaluation (MRR, Recall, Hit Rate)")
    parser.add_argument(
        "--testset",
        default="tests/evaluation/fixtures/testset.json",
        help="Path to testset JSON generated by generate_testset.py",
    )
    parser.add_argument("--k", type=int, default=5, help="Default k for retrieval (metrics computed for k=1,3,5,10)")
    parser.add_argument("--chroma_dir", default="./chroma_db", help="Path to Chroma DB directory")
    parser.add_argument(
        "--embed_model", default="intfloat/multilingual-e5-base", help="Embedding model name"
    )
    args = parser.parse_args()
    evaluate_retrieval(args.testset, args.k, args.chroma_dir, args.embed_model)
