"""
End-to-end RAGAS evaluation: faithfulness, answer_relevancy,
context_precision, context_recall, answer_correctness.

Requires:
  - Ollama running with the configured LLM model
  - The FastAPI server running (uvicorn src.api:app --port 8000)
  - A testset JSON generated by generate_testset.py

Usage:
    # Terminal 1
    uvicorn src.api:app --port 8000

    # Terminal 2
    python tests/evaluation/evaluate_e2e.py \
        --testset tests/evaluation/fixtures/testset.json \
        --api_url http://localhost:8000 \
        --k 5

CI pass thresholds (adjust after establishing your baseline):
  faithfulness       >= 0.70
  context_recall     >= 0.60
  answer_relevancy   >= 0.70
"""
import argparse
import json
import os
import sys
from datetime import datetime

sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", ".."))

CI_THRESHOLDS = {
    "faithfulness": 0.70,
    "context_recall": 0.60,
    "answer_relevancy": 0.70,
}


def _build_ragas_llm(provider: str):
    if provider == "ollama":
        from langchain_community.chat_models import ChatOllama
        model = os.getenv("LLM_MODEL", "gemma4:4b")
        return ChatOllama(model=model, temperature=0)
    elif provider == "claude":
        from langchain_anthropic import ChatAnthropic
        return ChatAnthropic(model="claude-haiku-4-5-20251001", temperature=0)
    elif provider == "openai":
        from langchain_openai import ChatOpenAI
        return ChatOpenAI(model="gpt-4o-mini", temperature=0)
    else:
        raise ValueError(f"Unknown provider: {provider}")


def evaluate_e2e(testset_path: str, api_url: str, k: int, judge_llm_provider: str):
    import httpx
    from datasets import Dataset
    from ragas import evaluate
    from ragas.metrics import faithfulness, answer_relevancy, context_precision, context_recall, answer_correctness
    from langchain_community.embeddings import HuggingFaceEmbeddings

    with open(testset_path, encoding="utf-8") as f:
        testset = json.load(f)

    print(f"Running E2E evaluation on {len(testset)} questions via {api_url}...")

    questions, answers, contexts_list, ground_truths = [], [], [], []

    with httpx.Client(base_url=api_url, timeout=120) as client:
        for i, item in enumerate(testset):
            question = item["question"]
            ground_truth = item.get("ground_truth", "")

            try:
                resp = client.post("/query", json={"question": question, "k": k})
                resp.raise_for_status()
                data = resp.json()
                answer = data["answer"]
            except Exception as e:
                print(f"  [WARN] Query failed for Q{i}: {e}")
                answer = ""

            questions.append(question)
            answers.append(answer)
            contexts_list.append(item.get("contexts", []))
            ground_truths.append(ground_truth)

            if (i + 1) % 10 == 0:
                print(f"  {i + 1}/{len(testset)} done")

    dataset = Dataset.from_dict({
        "question": questions,
        "answer": answers,
        "contexts": contexts_list,
        "ground_truth": ground_truths,
    })

    llm = _build_ragas_llm(judge_llm_provider)
    embeddings = HuggingFaceEmbeddings(model_name="intfloat/multilingual-e5-base")

    metrics = [faithfulness, answer_relevancy, context_precision, context_recall, answer_correctness]

    result = evaluate(
        dataset=dataset,
        metrics=metrics,
        llm=llm,
        embeddings=embeddings,
    )

    scores = result.to_pandas().mean(numeric_only=True).to_dict()

    print("\n--- RAGAS End-to-End Metrics ---")
    passed = True
    for metric, score in scores.items():
        threshold = CI_THRESHOLDS.get(metric)
        status = ""
        if threshold is not None:
            ok = score >= threshold
            status = " PASS" if ok else f" FAIL (threshold={threshold})"
            if not ok:
                passed = False
        print(f"  {metric:<30} {score:.4f}{status}")

    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    output_dir = os.path.join(os.path.dirname(__file__), "results")
    os.makedirs(output_dir, exist_ok=True)
    output_path = os.path.join(output_dir, f"ragas_{timestamp}.json")
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump({"timestamp": timestamp, "metrics": scores}, f, ensure_ascii=False, indent=2)
    print(f"\nSaved → {output_path}")

    if not passed:
        print("\nOne or more metrics failed CI thresholds.")
        sys.exit(1)


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="End-to-end RAGAS evaluation")
    parser.add_argument(
        "--testset",
        default="tests/evaluation/fixtures/testset.json",
        help="Path to testset JSON",
    )
    parser.add_argument("--api_url", default="http://localhost:8000", help="Base URL of the running FastAPI server")
    parser.add_argument("--k", type=int, default=5, help="Number of chunks to retrieve per query")
    parser.add_argument(
        "--judge_llm",
        default="ollama",
        choices=["ollama", "claude", "openai"],
        help="LLM provider for RAGAS judge metrics",
    )
    args = parser.parse_args()
    evaluate_e2e(args.testset, args.api_url, args.k, args.judge_llm)
